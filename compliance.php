			    <p> <b>2. Ultimate Beneficial Owners (UBO)</b><br/>
          A UBO is a natural person who holds ownership or control over a company, 
trust, or legal entity, directly benefiting from its assets and profits. Essentially, 
the UBO is the authentic individual steering the entity's decision-making, 
whether through direct or indirect means.
              </p>
			    <p>In a strategic move, the UAE has replaced Cabinet Decision No. 34 of 2020 with 
Cabinet Resolution No. 58 of 2020, specifically focusing on the Regulation of 
Procedures Related to Real Beneficiaries. This decision, aimed at enhancing 
transparency, outlines robust mechanisms and procedures to identify and 
regulate Ultimate Beneficial Ownership for entities registered in the UAE. The 
ultimate goal is to ensure international compliance, bolster global confidence, 
and fortify the UAE's position as a transparent business hub.
              </p>
			    <p> Within the framework of anti-money laundering and counter-terrorism 
financing laws, electronic money institutions are mandated to adhere to client 
identification obligations. A crucial facet of this obligation involves the 
identification of Ultimate Beneficial Owners (UBO) of their clients, aligning with 
international standards and contributing to the prevention of illicit financial 
activities.
              </p>
			    <p> <b>3. Country by Country Reporting (CbC Reporting): </b><br/>
          CbC Reporting is a crucial component of Action 13 under the Base Erosion and 
Profit Shifting (BEPS) initiative, led by the Organization for Economic Cooperation and Development (OECD) and the Group of Twenty (G20) 
industrialized nations.

              </p>
			    <p> BEPS Action 13 mandates large Multinational Groups of Entities (MNEs) to 
submit a CbC Report. This report breaks down the global revenue, pre-tax 
profits, accrued income tax, and other economic indicators for each jurisdiction 
where the MNE operates.
              </p>
              <ul>
              <li> <p> <b>Purpose of CbC Reporting:</b>
          The primary aim of CbC Reporting is to bridge the information gap between 
taxpayers and tax administrations. It ensures transparency on where economic 
value is generated within the MNE Group and verifies whether it aligns with 
profit allocation and tax payments on a global scale.
              </p></li>
              <li> <p> <b>UAE Compliance Criteria:</b>
              UAE-headquartered Groups meeting specific criteria are obliged to comply with 
CbC Reporting legislation. These criteria include being MNE Groups with 
residents for tax purposes in different jurisdictions and having a total 
consolidated revenue equal to or exceeding AED 3,150,000,000 for the 
preceding financial year.
              </p></li>
              <li> <p> <b>Tax Residency and Filing Deadlines:</b>
          Entities must submit a CbC Reporting (CbCR) notification, confirming their 
status as the Ultimate Parent Entity resident in the UAE. This includes essential 
identification details such as trade license number, address, business activity, 
and year-end.
              </p></li>

			   <li> <p> <b>CbC Report Content:</b>
          The CbC Report itself encompasses quantitative and qualitative information 
about the MNE Group. Key details, including revenues, profits, employee count, 
business description, etc., must be accurately reported.
              </p></li>
